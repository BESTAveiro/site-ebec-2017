    	fileName = RemoveAccents(fileName);
    	fileName = RemoveSpecialCharacters(fileName);
    	var parseFile = new Parse.File(fileName, file);


    	var curriculum = new Curriculum();
	    curriculum.set("name", name);
	    curriculum.set("email", email);
