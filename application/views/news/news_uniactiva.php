                <p class="text-justify"><b>UNIACTIVA</b> é parceira da EBEC Aveiro na modalidade de 
                <b><i>Team Design</i></b>.</p>

                <p class="text-justify">A UNIACTIVA é uma empresa que trabalha na área da segurança, 
                tendo como missão ajudar os seus clientes a construir a sua segurança, a da sua empresa e a 
                dos que com eles trabalham. Dispõem de vários serviços desde a protecção individual, alarmes de 
                intrusão, câmaras de video-vigilância a proteção anti-incêndios.</p>
                
                <p class="text-justify"><b>A prova da modalidade de <i>Team Design</i> será elaborada pela 
                UNIACTIVA</b>. As equipas terão 24 horas para resolver o desafio proposto pela empresa, cumprindo 
                com as regras estipulas no guião de prova e utilizando os materiais e ferramentas fornecidos pela 
                organização. <b>O júri</b> que irá avaliar os projetos finais será ainda <b>constituído por 
                2 membros da UNIACTIVA</b>.</p>

                <p class="text-justify">Esta é uma excelente oportunidade para os participantes de estabelecer 
                um contacto mais próximo com a empresa em questão e de por em prática os conhecimentos técnicos, 
                em situações reais do contexto empresarial.</p>

                <p>Os interessados devem formar uma equipa de 4 elementos e inscrever-se na modalidade de 
                <i>Team Design</i> em: <a href="<?php echo base_url('index.php/frontpage/apply')?>" target="_blank">http://bestaveiro.web.ua.pt/ebec/index.php/frontpage/apply</a> 
                até ao dia 26 de fevereiro.</p>

                <p>Para saberes mais informações acerca da UNIACTIVA, consulta:
                <a href=" http://www.uniactiva.pt/" target="_blank"> http://www.uniactiva.pt/</a></p>

