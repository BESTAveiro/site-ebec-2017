                <p class="text-justify"><b>XLM</b> presente na feira de empresas da EBEC Aveiro 2016.</p>

                <p class="text-justify">A XLM é uma empresa de inovação e tecnologia, focada em desenvolver as melhores 
                soluções de software e recursos humanos para os seus clientes. Está há 18 anos neste mercado e intervem 
                em 10 países diferentes.</p>
                
                <p class="text-justify">Apresenta soluções inovadoras no desenvolvimento de software, outsourcing e 
                serviços de consultaria na área da <b>indústria de IT</b>. A XLM cria softwares para empresas, usando 
                serviços e produtos de qualidade desenvolvidos com a tecnologia mais apropriada, metodologia eficaz 
                e sempre em cooperação com o cliente. Mais recentemente, tem vindo a fazer um investimento sério e 
                sustentável no sector das telecomunicações, faturação energética, sistemas de gestão e sistemas de 
                informação geográfica.</p>

                <p class="text-justify">O envolvimento em projetos exigentes, utilizando as melhores práticas de 
                desenvolvimento e gestão, promove diariamente o talento dos seus recursos. A empresa trabalha ainda, 
                no sentido de melhorar o ambiente de inovação dentro das suas equipas e parceiros, incentivando a 
                criatividade e a mudança de paradigmas tradicionais.</p>

                <p>Todos os alunos são convidados a aparecer na feira de empresas, no dia 3 de março no Complexo 
                Pedagógico e Cientifico da UA, onde a empresa XLM estará presente.</p>

                <p>Para saberes mais informações acerca da XLM, consulta:
