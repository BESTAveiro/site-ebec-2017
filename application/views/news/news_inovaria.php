                <p class="text-justify"><b>Inova-Ria</b> presente na feira de empresas da EBEC Aveiro 2016.</p>

                <p class="text-justify">A Inova-Ria – Associação de Empresas para uma Rede de Inovação em Aveiro é uma entidade sem fins lucrativos, que tem como objeto a criação e consolidação de um cluster na área das <b>Tecnologias de Informação, Comunicação e Eletrónica</b>, com especial enfoque nas telecomunicações. Com cerca de 60 empresas associadas, a Inova-Ria reúne no seu seio um conjunto valioso de empresas de tecnologia intensiva, start-ups, pequenas e médias empresas consolidadas e algumas empresas de grande dimensão.
</p>
                
                <p class="text-justify">Em dez anos de existência, a Inova-Ria evoluiu, desempenhando um papel ativo na dinamização e criação de valor no setor, designadamente através da investigação, do desenvolvimento de tecnologias, do conhecimento e formação de pessoas, de experiências internacionais e, sobretudo, da criação de empresas tecnológicas. Tudo isto em rede, promovendo estratégias colaborativas que aportem valor a todas as empresas e fomentem a criatividade e a competitividade.
</p>

                <p class="text-justify">Todos os alunos são convidados a aparecer na feira de empresas, no dia 3 de março no Complexo Pedagógico e Cientifico da UA, onde a Inova-Ria estará presente.
</p>

                <p>Para saberes mais informações acerca da InovaRIA consulta:  
