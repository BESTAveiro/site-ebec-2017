                de conhecer a atividade das entidades presentes, interagir com elas e haverá ainda um espaço dedicado a 
                entrevistas por parte das empresas presentes.</p>

                <p class="text-justify">Haverá ainda um espaço dedicado a entrevistas por parte da BOLD Internacional, 
                empresa de Consultoria em Tecnologias de Informação e Telecomunicações, Aplicações Móveis e Marketing 
                Digital.</p>

                <p class="text-justify">Estão confirmadas a presença na feira de empresas da Ergostart, da BOLD 
