                <br/><br/>

                <p class="text-justify"><b>iTGrow</b> oferece 4 estágios a estudantes de Informática, Telecomunicações, 
                Física e Matemática.</p>

                <p class="text-justify">A iTGrow é uma empresa participada equitativamente pelo Banco BPI e pela 
                Critical Software que tem como objetivo atrair, selecionar e complementar a formação de jovens engenheiros. 
                A iTGrow promove o desenvolvimento de competências valorizadas pelo mercado de IT através da exposição 
                dos seus colaboradores às melhores práticas, envolvendo-os em projetos exigentes e ambiciosos, no domínio 
                da Engenharia de Software.</p>
                
                <p class="text-justify">Na iTGrow podes encontrar um complemento da formação académica através de 
                ações de formação para competências técnicas e comportamentais, programas de tutoria para partilha 
                de experiências com engenheiros séniores e treinos on-the-job nas melhores práticas de Engenharia de 
                Software. A iTGrow proporciona ainda um acompanhamento próximo da evolução dos colaboradores à entrada 
                do mercado de trabalho.</p>

                <p class="text-justify">A iTGrow procura principalmente candidatos recém-formados com curso superior em 
                <b>Engenharia Informática, Eng. Electrotécnica, Eng. Biomédica, Telecomunicações, Física e Matemática</b>, 
                com conhecimentos avançados da língua inglesa e forte motivação para o trabalho e desafios na área de 
                Engenharia de Software.</p>

                <p>Os <b>participantes da EBEC Aveiro</b> interessados deverão submeter o seu currículo no 
                <a href="<?php echo base_url('index.php/frontpage/jobfair')?>" target="_blank">website da EBEC Aveiro</a> 
                até ao dia 26 de fevereiro, após esta data a iTGrow irá selecionar os melhores candidatos que serão 
                posteriormente anunciados.</p>

                <p>Para saberes mais informações acerca da iTGrow, consulta:
                <a href="http://www.itgrow.pt/" target="_blank">http://www.itgrow.pt/</a></p>

