                <h1 class="text-no-transform">BEST Aveiro associa-se à Cáritas Diocesana de Aveiro</h1>

                <!-- Date/Time -->
                <p><i class="fa fa-clock-o"></i>&nbsp; 2 MARÇO 2016</p>
                <br/>

                <!-- Image -->
                <img class="well img-responsive img-center" style="max-height: 400px; background: rgb(255, 255, 255);" 
                src="<?php echo base_url('assets/img/news/news-caritas-aveiro.jpg') ?>" alt="" >
                <br/><br/>

                <p class="text-justify">Ajuda-nos a ajudar, contribuindo para a recolha de alimentos para a 
                Cáritas Diocesana de Aveiro, na feira de empresas, dia 3 de março no Complexo Pedagógico e 
                Científico.</p>

                 <p class="text-justify">Este ano, a tua competição de engenharia EBEC Aveiro terá um caráter 
                 solidário, contribuindo com produtos alimentares para a Cáritas Diocesana de Aveiro.</p>

                <p class="text-justify">Qualquer pessoa pode contribuir nesta ação de solidariedade com produtos 
                alimentares da seguinte lista:</p>
                 <ul>
                  <li>Leite;</li>
                  <li>Arroz e massa;</li>
                  <li>Leguminosas secas;</li>
                  <li>Enlatados (atum, salsichas, feijão, grão, ervilhas, fruta, …);</li>
                  <li>Azeite e óleo;</li>
                  <li>Bolachas e cereais de pequeno-almoço;</li>
                  <li>Papas lácteas;</li>
                  <li>Farinha e açúcar.</li>
                </ul> 
                <br/>

                <p class="text-justify">O ponto de recolha será esta 5ª feira (dia 3) na banca do BEST Aveiro, 
                no Complexo Pedagógico e Cientifico da UA, durante a feira de empresas que estará aberta a toda 
                a comunidade académica.</p>

                <p class="text-justify">Para os participantes na competição da EBEC Aveiro, serão também recolhidos 
                alimentos no sábado antes do início da prova.</p>

                <p class="text-justify">Temos ainda o prazer de anunciar que o Presidente da Direção da 
                Cáritas Diocesana de Aveiro, Diácono José Ferreira Alves, estará presente na sessão de abertura 
                da EBEC Aveiro, onde irá fazer um breve discurso e apresentação da sua instituição.</p>

                <p class="text-justify">Vem à feira de empresas interagir e contactar com algumas empresas da 
                tua área e aproveita para ajudar quem mais precisa. O teu gesto pode fazer a diferença.</p>

                <p class="text-justify">A <b>Cáritas Diocesana de Aveiro</b> é o organismo oficial da Igreja Católica, 
                destinada à promoção e exercício de acção social. Incumbe em especial à Cáritas o apoio aos grupos 
                paroquiais de acção social, o fomento do voluntariado e a congregação de esforços tendentes à 
                prevenção e solução de problemas sociais. A Cáritas Diocesana de Aveiro dispõe de vários serviços 
                desde um centro de acolhimento e alojamento temporários, creche e pré-escolar e ainda um mini-banco 
                de ajudas técnicas.</p>

                <p>Para saberes informações acerca da Cáritas Diocesana de Aveiro consulta: 
                <a href="http://www.caritas.pt/aveiro/" target="_blank">http://www.caritas.pt/aveiro/</a></p>

