                <p class="text-justify"><b>Grupo Visabeira</b> presente na feira de empresas da EBEC Aveiro 2016.</p>

                <p class="text-justify">O Grupo Visabeira nasceu em 1980 em Viseu, Portugal, onde mantém a sua sede, 
                e é hoje uma holding multinacional organizada em cinco sub-holdings, Visabeira Global, Visabeira 
                Indústria, Visabeira Turismo, Visabeira Imobiliária, e Visabeira Participações Financeiras. Tem uma 
                presença destacada em dez países, comercializando os seus produtos e serviços em mais de cinco dezenas 
                de nações, nos cinco continentes.</p>
                
                <p class="text-justify">As atividades do grupo estão relacionadas com, infra-estruturas de 
                telecomunicações, electricidade, gás e construção civil, as quais reforçaram a sua importância 
                quer a nível nacional, quer internacional.</p>

                <p class="text-justify">Todos os alunos são convidados a aparecer na feira de empresas, no dia 3 de março 
                no Complexo Pedagógico e Cientifico da UA, onde o Grupo Visabeira estará presente.</p>

                <p>Para saberes mais informações acerca do Grupo Visabeira consulta: 
