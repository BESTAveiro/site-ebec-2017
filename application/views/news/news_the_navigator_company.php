                <p class="text-justify"><b>The Navigator Company</b> presente na feira de empresas da EBEC Aveiro 2016.</p>

                <p class="text-justify">A The Navigator Company é hoje uma das mais fortes presenças de Portugal no 
                mundo, desempenhando um papel estruturante para a economia nacional, pela integração vertical do seu 
                modelo de negócio: investigação aplicada, floresta, pasta de celulose, energia renovável e papel.</p>
                
                <p class="text-justify">É líder europeia na produção de papel fino de impressão e escrita não 
                revestido e na produção de pasta branqueada de eucalipto. Apesar disto, pautam a sua acção por 
                um conjunto de princípios e regras de gestão responsável, que conciliam preocupações ambientais, 
                sociais e económicas.</p>

                <p class="text-justify">O contributo do grupo vai para além da produção de pasta e papel. A 
                produção de energia a partir de biomassa é também uma área onde a The Navigator Company  ocupa 
                uma posição de destaque. Mais de 50% do total  da energia produzida em Portugal a partir de 
                biomassa é gerada pela The Navigator Company.</p>

                <p>Todos os alunos são convidados a aparecer na feira de empresas, no dia 3 de março no Complexo 
                Pedagógico e Cientifico da UA, onde a empresa The Navigator Company estará presente.</p>

                <p>Para saberes mais informações acerca da The Navigator Company consulta:
                <a href="http://www.thenavigatorcompany.com/" target="_blank">http://www.thenavigatorcompany.com/</a></p>

