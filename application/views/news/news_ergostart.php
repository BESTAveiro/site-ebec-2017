                <p class="text-justify">A ERGOSTART é parceira da EBEC Aveiro na modalidade de 
                <i>Case Study</i>.</p>

                <p class="text-justify">A ERGOSTART nasceu com o propósito de identificar e impulsionar 
                ideias inovadoras para o desenvolvimento de novos produtos com fim à sua industrialização. 
                Intrinsecamente ligada à empresa Nautilus, a incubadora de ideias ERGOSTART permitirá que 
                os empreendedores usufruam da capacidade produtiva da empresa, bem como de um programa de 
                tutoria com profissionais especializados. A ERGOSTART afigura-se como um espaço de 
                valorização mútua de competências entre os meios empresarial e universitário.</p>
                
                <p class="text-justify">A ERGOSTART procura ideias inovadoras para o desenvolvimento 
                de novos produtos cujo processo produtivo seja a injeção de polímeros, como ABS, PP, 
                PLA, etc. Procuram produtos para todos os setores de atividade e são elegíveis ideias 
                de produtos para uso doméstico, organizacional ou industrial.</p>

                <p class="text-justify">Os estudantes interessados poderão enviar o seu currículo através 
                do website da EBEC Aveiro, assim como preencher o currículo no 
                <a href="http://www.best.eu.org/student/career/index.jsp" target="_blank" >website do BEST</a>. 
                Estes serão disponibilizados à ERGOSTART e às restantes empresas.</p>

                <p class="text-justify">Todos os participantes do BEST Training Day receberão um 
                certificado de participação na formação.</p>

                <br>

                Para saberes mais informações, podes consultar 
                <a href="http://www.ergostart.pt/" target="_blank">http://www.ergostart.pt/</a>.</p>

