                <p class="text-justify">A BOLD Internacional apresenta-se como uma empresa inovadora no mercado de 
                consultoria em tecnologias de informação e telecomunicações e irá estar presente na feira de empresas 
                da EBEC Aveiro no dia 3 de março.</p>
                
                <p class="text-justify">A missão da BOLD é apresentar aos clientes soluções diferenciadoras e de 
                qualidade, que representem verdadeiros ativos para os seus negócios. Têm como objetivo inspirar as 
                pessoas a trabalhar com criatividade, paixão, otimismo e excelência, mas acima de tudo, entregar soluções 
                técnicas inovadoras e rentáveis que permitam a capitalização dos negócios dos seus clientes.</p>

                <p class="text-justify">A BOLD está interessada em estudantes à procura de novos desafios e com vontade 
                de aprender, e para isso irão realizar entrevistas durante a feira de empresas a estudantes que demonstrem 
                interesse.</p>

                <p class="text-justify">Os estudantes interessados deverão submeter o seu currículo no 
                <a href="<?php echo base_url('index.php/frontpage/jobfair')?>" target="_blank">website da EBEC Aveiro</a>
                até dia 26 de fevereiro, após esta data a BOLD irá selecionar os melhores candidatos que serão 
                contactados atempadamente.</p>

                <p class="text-justify"></p>

                <p class="text-justify">Todos os participantes do BEST Training Day receberão um 
